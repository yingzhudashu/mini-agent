/**
 * @file auto-optimizer.ts — 全自动优化编排器 (Phase 5)
 * @description
 *   Self-Optimization 子系统的 Phase 5 核心组件，负责串联整个自动优化流程：
 *
 *   1. 自我审视 (Inspection) → 分析代码质量、架构完整性、痛点
 *   2. 外部调研 (Research) → 搜索先进的架构模式和最佳实践
 *   3. 生成提案 (Proposal) → 基于痛点和调研生成优化方案
 *   4. Git 快照 (Snapshot) → 在变更前创建 Git 快照，用于回滚
 *   5. 执行优化 (Execution) → 应用文件变更，运行测试
 *   6. 自动修复 (Auto-fix) → 如果测试失败，调用 LLM 生成修复补丁
 *   7. 回滚 (Rollback) → 如果修复失败，回滚到 Git 快照
 *   8. 记录日志 (Logging) → 将结果写入 OPTIMIZATION_LOG.md
 *
 *   执行策略：
 *   - 低风险 (low) 提案：自动执行
 *   - 中风险 (medium) 提案：跳过（需要用户确认）
 *   - 高风险 (high/destructive) 提案：跳过（必须手动审核）
 *
 *   安全机制：
 *   - Git 快照：执行前创建，成功则 finalize，失败则 revert
 *   - 测试验证：每次变更后运行测试，确保不破坏现有功能
 *   - 自动修复上限：最多 2 次修复尝试（防死循环）
 *
 * @module core/self-opt/auto-optimizer
 */

import * as fs from "node:fs";
import * as path from "node:path";
import type {
  InspectionReport,
  ResearchReport,
  OptimizationProposal,
  OptimizationResult,
  OptimizationLogEntry,
  FileChange,
} from "./types.js";
import { generateProposals, generateFileChanges } from "./proposal-engine.js";
import { executeOptimization } from "./self-test-runner.js";
import {
  createSnapshot,
  revertToSnapshot,
  finalizeSnapshot,
  isInGitRepo,
} from "./git-snapshot.js";
import { generateFixDiff } from "./diff-generator.js";

