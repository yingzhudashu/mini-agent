/** 允许自动执行的风险等级（仅低风险） */
const AUTO_RISK = ["low"] as const;

// ============================================================================
// 优化日志管理
// ============================================================================

/**
 * 加载优化历史日志
 *
 * 从 OPTIMIZATION_LOG.md 中解析所有 JSON 记录，
 * 用于分析优化历史和趋势。
 *
 * @param projectRoot 项目根目录
 * @returns 优化日志条目列表
 *
 * @example
 * ```ts
 * const logs = loadOptimizationLog(projectRoot);
 * console.log(`共 ${logs.length} 次优化记录`);
 * ```
 */
export function loadOptimizationLog(
  projectRoot: string
): OptimizationLogEntry[] {
  const logPath = path.join(projectRoot, "OPTIMIZATION_LOG.md");
  if (!fs.existsSync(logPath)) return [];

  try {
    const content = fs.readFileSync(logPath, "utf-8");
    // 提取所有 JSON 代码块
    const blocks = content.match(/\{[\s\S]*?\}/g);
    if (!blocks) return [];

    const entries: OptimizationLogEntry[] = [];
    for (const b of blocks) {
      try {
        const e = JSON.parse(b);
        if (e.result && e.proposal) entries.push(e);
      } catch {
        // 忽略解析失败的块
      }
    }
    return entries;
  } catch {
    return [];
  }
}

/**
 * 追加优化日志记录
 *
 * 将单次优化的结果追加到 OPTIMIZATION_LOG.md 文件中。
 * 如果文件不存在，会自动创建并添加标题。
 *
 * @param projectRoot 项目根目录
 * @param entry 优化日志条目
 *
 * @example
 * ```ts
 * appendOptimizationLog(projectRoot, {
 *   result: optimizationResult,
 *   proposal: { id: "prop-1", type: "add", target: "测试", ... }
 * });
 * ```
 */
export function appendOptimizationLog(
  projectRoot: string,
  entry: OptimizationLogEntry
): void {
  const logPath = path.join(projectRoot, "OPTIMIZATION_LOG.md");
  const record = `## ${new Date().toISOString()}\n\n\`\`\`json\n${JSON.stringify(
    entry,
    null,
    2
  )}\n\`\`\`\n\n---\n\n`;

  if (!fs.existsSync(logPath)) {
    // 创建新文件，添加标题
    fs.writeFileSync(
      logPath,
      `# Optimization Log\n\n> Auto-generated by Mini Agent Self-Optimization System\n\n---\n\n${record}`,
      "utf-8"
    );
  } else {
    // 追加到现有文件
    fs.appendFileSync(logPath, record, "utf-8");
  }
}

