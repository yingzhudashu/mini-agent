/**
 * 自动优化结果接口
 */
export interface AutoOptimizeResult {
  /** 自我审视报告 */
  inspection: InspectionReport;
  /** 外部调研报告 */
  research: ResearchReport;
  /** 所有生成的提案 */
  proposals: OptimizationProposal[];
  /** 已执行的提案及结果 */
  executed: ExecResult[];
  /** 跳过的提案（中高风险） */
  skipped: OptimizationProposal[];
  /** 执行摘要 */
  summary: string;
}

/**
 * 全自动优化主函数
 *
 * 这是 `.optimize auto` 命令的入口点，串联整个优化流程：
 *
 * ```
 * [1/5] 自我审视 → Inspector 分析代码质量
 * [2/5] 外部调研 → Researcher 搜索最佳实践
 * [3/5] 生成提案 → ProposalEngine 生成优化方案
 * [4/N] 执行提案 → 应用变更 + 测试 + 自动修复 + Git 快照
 * [5/5] 输出报告 → 格式化结果
 * ```
 *
 * @param srcDir 项目 src/ 目录路径
 * @param projectRoot 项目根目录
 * @returns 自动优化结果
 *
 * @example
 * ```ts
 * const projectRoot = process.cwd();
 * const srcDir = path.join(projectRoot, "src");
 * const result = await autoOptimize(srcDir, projectRoot);
 * console.log(result.summary);
 * // 输出: "执行 1，成功 1，失败 0，回滚 0"
 * ```
 */
export async function autoOptimize(
  srcDir: string,
  projectRoot: string
): Promise<AutoOptimizeResult> {
  // ── Step 1: 自我审视 ──
  console.log("\n[1/5] 🔍 自我审视...");
  const { inspectSelf } = await import("./inspector.js");
  const insp = await inspectSelf(srcDir);
  console.log(
    `  架构: ${
      insp.architectureChecks.filter((c) => c.passed).length
    }/${insp.architectureChecks.length}`
  );
  console.log(
    `  未测试模块: ${
      insp.moduleAnalysis.filter((m) => !m.hasTests && m.linesOfCode > 50)
        .length
    } 个`
  );

  // ── Step 2: 外部调研 ──
  console.log("\n[2/5] 🌐 外部调研...");
  const { researchExternal } = await import("./researcher.js");
  const res = await researchExternal();
  console.log(`  资源: ${res.references.length} 个`);

  // ── Step 3: 生成提案 ──
  console.log("\n[3/5] 📋 生成提案...");
  const proposals = generateProposals(insp, res);
  // 为每个提案生成文件变更
  for (const p of proposals) {
    generateFileChanges(p, insp);
  }

  // 分类：自动执行 vs 跳过
  const auto = proposals.filter((p) =>
    AUTO_RISK.includes(p.riskLevel as "low")
  );
  const skipped = proposals.filter(
    (p) => !AUTO_RISK.includes(p.riskLevel as "low")
  );
  console.log(
    `  总计: ${proposals.length}, 自动: ${auto.length}, 跳过: ${skipped.length}`
  );

  // ── Step 4: 执行提案 ──
  const useGit = await isInGitRepo(projectRoot);
  const executed: ExecResult[] = [];

  for (let i = 0; i < auto.length; i++) {
    console.log(
      `\n[4/${auto.length}] 📌 执行: ${auto[i].target} (${auto[i].files.length} 文件变更)`
    );

    const r = await execProposal(auto[i], projectRoot, srcDir, useGit);
    executed.push(r);

    // 记录到日志
    appendOptimizationLog(projectRoot, {
      result: r.result,
      proposal: {
        id: r.proposal.id,
        type: r.proposal.type,
        target: r.proposal.target,
        description: r.proposal.description,
        riskLevel: r.proposal.riskLevel,
      },
    });

    console.log(
      `  ${r.result.status === "success" ? "✅" : "❌"} ${r.result.lesson}`
    );
    if (r.gitSnapshot) {
      console.log(`  📸 快照: ${r.gitSnapshot.slice(0, 8)}`);
    }
  }

  // ── Step 5: 生成摘要 ──
  const ok = executed.filter((e) => e.result.status === "success").length;
  const fail = executed.filter((e) => e.result.status === "failed").length;
  const rv = executed.filter((e) => e.result.reverted).length;
  const summary =
    `执行 ${executed.length}，成功 ${ok}，失败 ${fail}，回滚 ${rv}` +
    (skipped.length > 0 ? `，${skipped.length} 个跳过` : "");

  return {
    inspection: insp,
    research: res,
    proposals,
    executed,
    skipped,
    summary,
  };
}

