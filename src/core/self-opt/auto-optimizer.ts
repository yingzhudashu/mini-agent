/**
 * 将自动优化结果格式化为可读文本
 *
 * 用于 CLI 展示，包含：
 * - 执行摘要（总数、成功、失败、回滚）
 * - 每个已执行提案的结果（测试通过数、快照哈希）
 * - 跳过的提案列表
 *
 * @param r 自动优化结果
 * @returns 格式化的文本字符串
 *
 * @example
 * ```ts
 * const result = await autoOptimize(srcDir, projectRoot);
 * console.log(formatAutoOptimizeResult(result));
 * // 输出:
 * // ═══════════════════════════════════════════════════
 * // 🚀 Auto-Optimization Report
 * // ═══════════════════════════════════════════════════
 * // 执行 1，成功 1，失败 0，回滚 0
 * //
 * //   ✅ 添加缺失的测试文件: 所有测试通过，优化成功
 * //      测试: 11/11
 * //      快照: 6e3a563b
 * // ```
 */
export function formatAutoOptimizeResult(r: AutoOptimizeResult): string {
  const lines = [
    "═══════════════════════════════════════════════════",
    "🚀 Auto-Optimization Report",
    "═══════════════════════════════════════════════════",
    r.summary,
    "",
  ];

  for (const e of r.executed) {
    lines.push(
      `  ${e.result.status === "success" ? "✅" : "❌"} ${e.proposal.target}: ${
        e.result.lesson
      }`
    );
    lines.push(
      `     测试: ${e.result.testSummary.passed}/${e.result.testSummary.total}`
    );
    if (e.gitSnapshot) {
      lines.push(`     快照: ${e.gitSnapshot.slice(0, 8)}`);
    }
  }

  if (r.skipped.length > 0) {
    lines.push("", "⏭️ 跳过:");
    for (const s of r.skipped) {
      lines.push(
        `  ${s.riskLevel === "medium" ? "🟡" : "🔴"} ${s.target}`
      );
    }
  }

  return lines.join("\n");
}
