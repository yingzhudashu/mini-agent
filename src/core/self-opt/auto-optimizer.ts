/**
 * 单个提案执行结果
 *
 * 包含提案、优化结果、Git 快照哈希和回滚状态。
 */
interface ExecResult {
  /** 优化提案 */
  proposal: OptimizationProposal;
  /** 优化执行结果 */
  result: OptimizationResult;
  /** Git 快照哈希（执行前） */
  gitSnapshot?: string;
  /** 是否已回滚 */
  reverted: boolean;
}

// ============================================================================
// 内部辅助函数
// ============================================================================

/**
 * 应用文件变更到磁盘
 *
 * 根据 FileChange[] 创建、修改或删除文件。
 * 会自动创建不存在的父目录。
 *
 * @param changes 文件变更列表
 * @param cwd 工作目录（项目根目录）
 * @returns 操作结果
 */
async function applyChanges(
  changes: FileChange[],
  cwd: string
): Promise<{ success: boolean; error?: string }> {
  try {
    for (const f of changes) {
      const full = path.isAbsolute(f.path) ? f.path : path.join(cwd, f.path);

      if (f.action === "create" || f.action === "modify") {
        // 创建父目录（如果不存在）
        const dir = path.dirname(full);
        if (!fs.existsSync(dir)) {
          fs.mkdirSync(dir, { recursive: true });
        }
        // 写入文件内容
        if (f.content) {
          fs.writeFileSync(full, f.content, "utf-8");
        }
      } else if (f.action === "delete" && fs.existsSync(full)) {
        // 删除文件
        fs.unlinkSync(full);
      }
    }
    return { success: true };
  } catch (e: any) {
    return { success: false, error: e?.message };
  }
}

/**
 * 执行单个优化提案
 *
 * 工作流程：
 * 1. 创建 Git 快照（用于回滚）
 * 2. 确保有文件变更（如果没有，调用 generateFileChanges 或 diff-generator）
 * 3. 应用文件变更
 * 4. 运行测试
 * 5. 如果测试失败，尝试自动修复（由 executeOptimization 处理）
 * 6. 如果成功，finalize Git 快照；如果失败，回滚
 *
 * @param p 优化提案
 * @param cwd 项目根目录
 * @param srcDir 源码目录
 * @param useGit 是否使用 Git 快照
 * @returns 执行结果
 */
async function execProposal(
  p: OptimizationProposal,
  cwd: string,
  srcDir: string,
  useGit: boolean
): Promise<ExecResult> {
  let snap: string | null = null;

  // ── Step 1: 创建 Git 快照 ──
  if (useGit) {
    console.log(`  📸 创建快照: ${p.target}`);
    snap = await createSnapshot(cwd, `pre-${p.target}`);
    if (snap) console.log(`  📸 ${snap.slice(0, 8)}`);
  }

  // ── Step 2: 确保有文件变更 ──
  if (p.files.length === 0) {
    // 方法 1: 调用 generateFileChanges 基于模板生成
    const { inspectSelf } = await import("./inspector.js");
    const insp = await inspectSelf(srcDir);
    generateFileChanges(p, insp);

    // 方法 2: 如果还是没有，用 diff-generator 生成
    if (p.files.length === 0) {
      const fake = p.testCases.map((tc) => ({
        testCaseId: tc.id,
        passed: false,
        output: "待生成",
        durationMs: 0,
      }));
      const diff = await generateFixDiff(p, fake, srcDir);
      if (diff.success) p.files = diff.changes;
    }
  }

  // ── Step 3: 执行优化（含测试和自动修复） ──
  const result = await executeOptimization(p, cwd, async () =>
    applyChanges(p.files, cwd)
  );

  // ── Step 4: 处理 Git 快照 ──
  if (useGit && snap) {
    if (result.status === "success") {
      // 成功：finalize 快照（修改 commit message）
      await finalizeSnapshot(cwd, snap, `[self-opt] ${p.target}`);
    } else {
      // 失败：回滚到快照
      const rv = await revertToSnapshot(cwd, snap);
      result.reverted = rv.success;
      if (rv.success) {
        result.lesson += " [已回滚]";
      } else {
        result.lesson += ` [回滚失败: ${rv.error}]`;
      }
    }
  }

  return {
    proposal: p,
    result,
    gitSnapshot: snap ?? undefined,
    reverted: result.reverted,
  };
}

